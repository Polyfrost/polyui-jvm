package org.polyfrost.polyui.animate

/**
 * Set animations that are used with the [org.polyfrost.polyui.component.Component.set] method.
 * These animations are used to animate the transition of a component from one state to another.
 *
 * @since 1.11.3
 */
enum class SetAnimation {
    None, Fade, SlideLeft, SlideRight
}
